#!/usr/bin/env python3
"""
Data-Driven 4-Channel Residuals Preprocessor

This script generates 4-channel residuals from trained data-driven models
for fair baseline comparisons. Unlike the old version, this does NOT duplicate
residuals to create 8 channels - it only outputs the 4 data residual channels.

This ensures fair comparison with PINN models that have different physics
and data residuals.
"""

