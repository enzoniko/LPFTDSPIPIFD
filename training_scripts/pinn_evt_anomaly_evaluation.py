def sample_sequential_data(residuals_dict: Dict[str, np.ndarray], sample_length: int = 1000,
                         num_samples_per_condition: int = 10) -> Dict[str, List[Dict[str, np.ndarray]]]:
    """
    Sample sequential data segments from residuals for efficient evaluation.

    Args:
        residuals_dict: Dictionary of residuals from preprocess_pinn_data
        sample_length: Length of each sequential sample (default: 1000)
        num_samples_per_condition: Number of samples to extract per condition

    Returns:
        Dictionary with condition keys and lists of sample dictionaries
    """
    sampled_data = {}

    for condition_key, residuals in residuals_dict.items():
        condition_samples = []

        # Get the total length available
        total_length = len(residuals['data_res1'])  # All residual types have same length

        if total_length < sample_length:
            # If data is shorter than sample length, use the entire sequence
            sample_data = {}
            for residual_key, residual_data in residuals.items():
                sample_data[residual_key] = residual_data
            condition_samples.append({'data': sample_data})
        else:
            # Extract multiple sequential samples
            for i in range(min(num_samples_per_condition, total_length // sample_length)):
                start_idx = i * sample_length
                end_idx = start_idx + sample_length

                sample_data = {}
                for residual_key, residual_data in residuals.items():
                    sample_data[residual_key] = residual_data[start_idx:end_idx]

                condition_samples.append({'data': sample_data})

        sampled_data[condition_key] = condition_samples

    return sampled_data

def prepare_anomaly_detection_data(sampled_data: Dict[str, List[Dict[str, np.ndarray]]],
                                 normal_train_ratio: float = 0.7) -> Tuple[List[Dict], Dict[str, List[Dict]]]:
    """
    Prepare data for anomaly detection by splitting normal data and organizing test data.

    Args:
        sampled_data: Dictionary of sampled data by condition
        normal_train_ratio: Ratio of normal data to use for training

    Returns:
        Tuple of (normal_train_samples, test_samples_dict)
    """
    # Extract normal samples
    normal_samples = sampled_data.get('normal', [])
    if not normal_samples:
        raise ValueError("No normal samples found for training")

    # Split normal samples for training and testing
    np.random.shuffle(normal_samples)
    split_idx = int(normal_train_ratio * len(normal_samples))
    normal_train = normal_samples[:split_idx]
    normal_test = normal_samples[split_idx:]

    # Prepare test samples (normal test + all anomaly conditions)
    test_samples = {'normal': normal_test}
    for condition_key, samples in sampled_data.items():
        if condition_key != 'normal':
            test_samples[condition_key] = samples

    return normal_train, test_samples

def compute_evt_threshold_stability(normal_samples: List[Dict], feature_extractor,
                                  num_fragments: int = 5,
                                  agg_func=np.max, threshold_u_quantile=0.95, quantile=0.99,
                                  apply_pca=False, pca_n_components=None, normalize=True) -> Dict[str, float]:
    """
    Compute EVT threshold stability across different fragments of healthy residuals.

    Args:
        normal_samples: List of normal training samples
        feature_extractor: Function to extract features from samples
        num_fragments: Number of fragments to split healthy data into
        agg_func, threshold_u_quantile, quantile: EVT parameters
        apply_pca, pca_n_components, normalize: Feature processing parameters

    Returns:
        Dictionary with mean and std of thresholds across fragments
    """
    if len(normal_samples) < num_fragments:
        print(f"Warning: Only {len(normal_samples)} samples available, using {len(normal_samples)} fragments")
        num_fragments = len(normal_samples)

    # Shuffle samples to ensure random fragmentation
    samples_copy = normal_samples.copy()
    np.random.shuffle(samples_copy)

    # Split into fragments
    fragment_size = len(samples_copy) // num_fragments
    thresholds = []

    for i in range(num_fragments):
        start_idx = i * fragment_size
        if i == num_fragments - 1:
            # Last fragment gets remaining samples
            end_idx = len(samples_copy)
        else:
            end_idx = (i + 1) * fragment_size

        fragment_samples = samples_copy[start_idx:end_idx]

        if len(fragment_samples) == 0:
            continue

        try:
            # Create and fit EVT detector on this fragment
            detector = EVTDetector(
                feature_extractor=feature_extractor,
                agg_func=agg_func,
                threshold_u_quantile=threshold_u_quantile,
                quantile=quantile,
                apply_pca=apply_pca,
                pca_n_components=pca_n_components,
                normalize=normalize
            )

            detector.fit(fragment_samples, sample_rate=50000)
            thresholds.append(detector.threshold)

        except Exception as e:
            print(f"Warning: Failed to compute threshold for fragment {i+1}: {e}")
            continue

    if not thresholds:
        return {"mean_threshold": np.nan, "std_threshold": np.nan, "num_fragments": 0}

    thresholds = np.array(thresholds)

    return {
        "mean_threshold": float(np.mean(thresholds)),
        "std_threshold": float(np.std(thresholds)),
        "num_fragments": len(thresholds),
        "threshold_values": thresholds.tolist()
    }

def extract_features_from_residuals(sample_dict: Dict[str, np.ndarray]) -> np.ndarray:
    """
    Extract feature vector from residual sample dictionary.

    Args:
        sample_dict: Dictionary containing residual data

    Returns:
        Feature vector as numpy array
    """
    # Get data from the sample
    if 'data' in sample_dict:
        residuals = sample_dict['data']
    else:
        residuals = sample_dict

    # Combine all residual types into a single feature vector
    features = []
    for residual_key in ['data_res1', 'data_res2', 'data_res3', 'data_res4',
                        'phys_res1', 'phys_res2', 'phys_res3', 'phys_res4']:
        if residual_key in residuals:
            data = residuals[residual_key]

            # Handle different shapes consistently
            if isinstance(data, np.ndarray):
                # Flatten any multidimensional arrays to 1D
                if data.ndim > 1:
                    data = data.flatten()
                features.extend(data)
            else:
                # If not a numpy array, try to convert it
                try:
                    data_array = np.array(data).flatten()
                    features.extend(data_array)
                except Exception as e:
                    print(f"Warning: Could not process {residual_key}: {e}")
                    continue

    # Ensure we have a valid feature vector
    if len(features) == 0:
        print("Warning: No features extracted, returning zeros")
        return np.zeros(8)  # Return 8 zeros as fallback

    try:
        return np.array(features, dtype=np.float64)
    except Exception as e:
        print(f"Error creating feature array: {e}")
        return np.zeros(len(features))  # Return zeros with same length

def evaluate_pinn_model_evt(model_name: str, chunk_size_mb: int = 100,
                          sample_length: int = 1000, num_samples_per_condition: int = 10,
                          use_bayesian_models: bool = True, use_data_driven: bool = False,
                          data_driven_model_path: str = None) -> Dict:
    """
    Evaluate EVT anomaly detection for a specific PINN or data-driven model.

    Args:
        model_name: Name of the PINN model ('relobralo', 'constant_weight', 'brdr', 'pecann')
                    or 'data_driven_reg', 'data_driven_standard' for data-driven models
        chunk_size_mb: Chunk size for memory-efficient processing
        sample_length: Length of sequential samples
        num_samples_per_condition: Number of samples per condition
        use_bayesian_models: If True, use models from Bayesian optimization instead of retrained models
        use_data_driven: If True, use data-driven model instead of PINN
        data_driven_model_path: Specific path to data-driven model directory (optional)

    Returns:
        Dictionary containing evaluation results
    """
    if use_data_driven:
        print(f"\n[INFO] Evaluating EVT for data-driven model")
    else:
        print(f"\n[INFO] Evaluating EVT for {model_name} model")
    print("=" * 60)

    # Get condition mappings
    condition_mapping = get_condition_mapping()
    data_dir = "Data/v3"

    # Process each condition
    all_residuals = {}
    for condition_key, data_file in condition_mapping.items():
        data_path = os.path.join(data_dir, f"{data_file}.pth")

        if not os.path.exists(data_path):
            print(f"   [WARNING] Data file not found: {data_path}")
            continue

        print(f"   [DATA] Processing condition: {condition_key}")

        try:
            if use_data_driven:
                # Use data-driven preprocessing
                from training_scripts.data_driven_preprocessing import preprocess_data_driven_data
                residuals = preprocess_data_driven_data(data_path, chunk_size_mb=chunk_size_mb)
            else:
                # Generate residuals using PINN preprocessing
                residuals = preprocess_pinn_data(model_name, data_path, chunk_size_mb=chunk_size_mb)

            # Limit to first sample_length * num_samples_per_condition points for efficiency
            max_points = sample_length * num_samples_per_condition
            for residual_key in residuals:
                if len(residuals[residual_key]) > max_points:
                    residuals[residual_key] = residuals[residual_key][:max_points]

            all_residuals[condition_key] = residuals

        except Exception as e:
            print(f"      [ERROR] Failed to process {condition_key}: {e}")
            continue

    if not all_residuals:
        return {"error": f"No valid data processed for {model_name}"}

    # Sample sequential data segments
    print(f"   [SAMPLING] Sampling sequential data ({sample_length} points per sample)")
    sampled_data = sample_sequential_data(all_residuals, sample_length, num_samples_per_condition)

    # Prepare data for anomaly detection
    try:
        normal_train, test_samples = prepare_anomaly_detection_data(sampled_data)
        print(f"   [TRAINING] Training on {len(normal_train)} normal samples")
        print(f"   [TESTING] Testing on {sum(len(samples) for samples in test_samples.values())} total samples")
    except Exception as e:
        return {"error": f"Failed to prepare anomaly detection data: {e}"}

    # Define EVT parameter grid for optimization
    # We'll use extended features (True) to ensure proper feature extraction
    param_grid = {
        'agg_func': [np.max, np.mean, np.median],
        'threshold_u_quantile': [0.90, 0.95, 0.99],
        'quantile': [0.95, 0.99, 0.995],
        'use_extended_features': [True],  # Always use extended features for PINN residuals
        'apply_pca': [False, True],
        'pca_n_components': [None],  # Use None to keep all components, avoid dimension issues
        'normalize': [True, False]
    }

    # Create custom feature extractor that properly handles PINN residual dictionaries
    def pinn_feature_extractor(sample, sample_rate=1.0):
        """Extract features from PINN residual samples."""
        try:
            # Extract features from the residual dictionary
            features = extract_features_from_residuals(sample)

            # Ensure we return a proper numpy array
            if isinstance(features, np.ndarray):
                return features.astype(np.float64)
            else:
                return np.array(features, dtype=np.float64)
        except Exception as e:
            print(f"Error in feature extraction: {e}")
            # Return a reasonable fallback based on sample length
            if 'data' in sample and 'data_res1' in sample['data']:
                sample_length = len(sample['data']['data_res1'])
                expected_features = sample_length * 8  # 8 residual types
                return np.zeros(expected_features, dtype=np.float64)
            else:
                return np.zeros(8000, dtype=np.float64)  # 1000 samples * 8 residuals

    # Create a wrapper for the grid search that uses our custom feature extractor
    def custom_evaluate_grid_worker(method_class, params, normal_train, test_samples, sample_rate=1.0):
        """Custom worker that uses our PINN-specific feature extractor."""
        try:
            # Extract feature extraction parameters
            use_extended = params.pop('use_extended_features', True)
            apply_pca = params.pop('apply_pca', False)
            pca_n_components = params.pop('pca_n_components', None)

            # Always use our custom feature extractor for PINN residuals
            feature_extractor_local = pinn_feature_extractor

            # Initialize and fit detector with custom extractor
            detector = method_class(
                feature_extractor=feature_extractor_local,
                apply_pca=apply_pca,
                pca_n_components=pca_n_components,
                **params
            )

            detector.fit(normal_train, sample_rate=sample_rate)

            # Evaluate detector
            metrics = evaluate_detector(detector, test_samples)

            # Restore original parameters
            params.update({
                'use_extended_features': use_extended,
                'apply_pca': apply_pca,
                'pca_n_components': pca_n_components
            })

            return (params, metrics, metrics['f1'] if metrics else -np.inf)

        except Exception as e:
            print(f"Error in custom evaluation worker: {e}")
            return (params, None, -np.inf)

    # Run custom grid search
    print(f"   [OPTIMIZATION] Running EVT parameter optimization...")
    start_time = time.time()

    # Generate all parameter combinations
    from itertools import product
    keys = list(param_grid.keys())
    params_list = [dict(zip(keys, v)) for v in product(*param_grid.values())]

    # Initialize tracking variables
    best_f1 = -np.inf
    best_params = None
    best_metrics = None
    best_detector = None

    print(f"      Testing {len(params_list)} parameter combinations...")

    # Evaluate each parameter combination
    for i, params in enumerate(params_list):
        if (i + 1) % 10 == 0:
            print(f"      Progress: {i+1}/{len(params_list)} combinations tested")

        params_copy = params.copy()
        result_params, metrics, f1 = custom_evaluate_grid_worker(
            EVTDetector, params_copy, normal_train, test_samples, sample_rate=50000
        )

        if f1 > best_f1:
            best_f1 = f1
            best_params = result_params
            best_metrics = metrics

            # Recreate the best detector for later use
            try:
                best_detector = EVTDetector(
                    feature_extractor=pinn_feature_extractor,
                    apply_pca=best_params.get('apply_pca', False),
                    pca_n_components=best_params.get('pca_n_components', None),
                    agg_func=best_params.get('agg_func', np.max),
                    threshold_u_quantile=best_params.get('threshold_u_quantile', 0.95),
                    quantile=best_params.get('quantile', 0.99),
                    normalize=best_params.get('normalize', True)
                )
                best_detector.fit(normal_train, sample_rate=50000)
            except Exception as e:
                print(f"Warning: Could not recreate best detector: {e}")

    # Final fallback if no valid results
    if best_detector is None:
        print("      Creating fallback detector...")
        best_detector = EVTDetector(
            feature_extractor=pinn_feature_extractor,
            agg_func=np.max,
            threshold_u_quantile=0.95,
            quantile=0.99
        )
        try:
            best_detector.fit(normal_train, sample_rate=50000)
            best_metrics = evaluate_detector(best_detector, test_samples)
            best_params = {
                'agg_func': np.max,
                'threshold_u_quantile': 0.95,
                'quantile': 0.99,
                'use_extended_features': True,
                'apply_pca': False,
                'pca_n_components': None,
                'normalize': True
            }
        except Exception as e:
            print(f"Error creating fallback detector: {e}")
            best_metrics = None
            best_params = {}

    optimization_time = time.time() - start_time

    # Compute EVT threshold stability if we have a valid detector
    threshold_stability = None
    if best_detector is not None and len(normal_train) >= 3:  # Need at least 3 samples for meaningful stability check
        print(f"   [STABILITY] Computing EVT threshold stability across {min(5, len(normal_train))} fragments...")
        try:
            threshold_stability = compute_evt_threshold_stability(
                normal_train,
                feature_extractor=pinn_feature_extractor,
                num_fragments=min(5, len(normal_train)),  # Use up to 5 fragments
                agg_func=best_params.get('agg_func', np.max),
                threshold_u_quantile=best_params.get('threshold_u_quantile', 0.95),
                quantile=best_params.get('quantile', 0.99),
                apply_pca=best_params.get('apply_pca', False),
                pca_n_components=best_params.get('pca_n_components', None),
                normalize=best_params.get('normalize', True)
            )
            print(f"   [STABILITY] Mean threshold: {threshold_stability['mean_threshold']:.6f}, "
                  f"Std: {threshold_stability['std_threshold']:.6f} "
                  f"(across {threshold_stability['num_fragments']} fragments)")
        except Exception as e:
            print(f"Warning: Failed to compute threshold stability: {e}")
            threshold_stability = {"error": str(e)}

    # Compile results
    if best_metrics is not None:
        results = {
            'model_name': model_name,
            'optimization_time_seconds': optimization_time,
            'best_parameters': best_params,
            'best_metrics': best_metrics,
            'threshold_stability': threshold_stability,
            'num_conditions_processed': len(all_residuals),
            'sample_length': sample_length,
            'num_samples_per_condition': num_samples_per_condition,
            'total_training_samples': len(normal_train),
            'total_test_samples': sum(len(samples) for samples in test_samples.values()),
            'conditions': list(all_residuals.keys())
        }
    else:
        results = {
            'model_name': model_name,
            'error': 'Failed to obtain valid metrics from EVT evaluation',
            'optimization_time_seconds': optimization_time,
            'best_parameters': best_params or {},
            'threshold_stability': threshold_stability,
            'num_conditions_processed': len(all_residuals),
            'sample_length': sample_length,
            'num_samples_per_condition': num_samples_per_condition,
            'total_training_samples': len(normal_train) if 'normal_train' in locals() else 0,
            'total_test_samples': sum(len(samples) for samples in test_samples.values()) if 'test_samples' in locals() else 0,
            'conditions': list(all_residuals.keys())
        }

        print(f"   [ERROR] EVT evaluation failed for {model_name}")

    print(f"   [SUCCESS] Completed EVT evaluation for {model_name}")
    print(f"   [TIME] Optimization time: {optimization_time:.2f} seconds")
    print(f"   [RESULT] Best F1 Score: {best_metrics.get('f1', 0) if best_metrics else 0:.4f}")

    return results

def save_evaluation_results(all_results: Dict[str, Dict], output_dir: str = "pinn_evt_evaluation_results"):
    """
    Save evaluation results to files.

    Args:
        all_results: Dictionary mapping model names to results
        output_dir: Output directory for results
    """
    os.makedirs(output_dir, exist_ok=True)

    # Save individual model results
    for model_name, results in all_results.items():
        if 'error' not in results:
            output_file = os.path.join(output_dir, f"{model_name}_evt_results.json")
            save_results({model_name: results}, output_file)
            print(f"   [SAVE] Saved results for {model_name} to {output_file}")

    # Create summary report
    summary_file = os.path.join(output_dir, "pinn_evt_evaluation_summary.json")

    # Prepare summary data
    summary_data = {}
    for model_name, results in all_results.items():
        if 'error' not in results and results.get('best_metrics') is not None:
            metrics = results.get('best_metrics', {})
            threshold_stability = results.get('threshold_stability', {})
            summary_data[model_name] = {
                'f1_score': metrics.get('f1', 0),
                'accuracy': metrics.get('accuracy', 0),
                'precision': metrics.get('precision', 0),
                'recall': metrics.get('recall', 0),
                'auc': metrics.get('auc', 0),
                'optimization_time_seconds': results.get('optimization_time_seconds', 0),
                'num_conditions': results.get('num_conditions_processed', 0),
                'total_samples': results.get('total_test_samples', 0),
                'threshold_mean': threshold_stability.get('mean_threshold', np.nan) if threshold_stability else np.nan,
                'threshold_std': threshold_stability.get('std_threshold', np.nan) if threshold_stability else np.nan,
                'threshold_fragments': threshold_stability.get('num_fragments', 0) if threshold_stability else 0
            }
        else:
            # Include failed models with zero scores
            threshold_stability = results.get('threshold_stability', {})
            summary_data[model_name] = {
                'f1_score': 0,
                'accuracy': 0,
                'precision': 0,
                'recall': 0,
                'auc': 0,
                'optimization_time_seconds': results.get('optimization_time_seconds', 0),
                'num_conditions': results.get('num_conditions_processed', 0),
                'total_samples': results.get('total_test_samples', 0),
                'threshold_mean': threshold_stability.get('mean_threshold', np.nan) if threshold_stability else np.nan,
                'threshold_std': threshold_stability.get('std_threshold', np.nan) if threshold_stability else np.nan,
                'threshold_fragments': threshold_stability.get('num_fragments', 0) if threshold_stability else 0,
                'error': results.get('error', 'Unknown error')
            }

    # Save summary
    with open(summary_file, 'w') as f:
        json.dump(summary_data, f, indent=2)

    print(f"   [SAVE] Saved summary to {summary_file}")

