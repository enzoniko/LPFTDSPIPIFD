def load_data_driven_model(model_name: str, device: torch.device):
    """
    Load a data-driven model.

    Args:
        model_name: Either 'data_standard' or 'data_reg'
        device: Device to load the model on

    Returns:
        Tuple of (model, scaling_params)
    """
    # Map model names to paths
    if model_name == 'data_standard':
        data_driven_model_path = "results/data_driven_standard"
    elif model_name == 'data_reg':
        data_driven_model_path = "results/data_driven_reg"
    else:
        raise ValueError(f"Unknown data-driven model: {model_name}. Use 'data_standard' or 'data_reg'")

    # Ensure path is absolute
    if not os.path.isabs(data_driven_model_path):
        project_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        data_driven_model_path = os.path.join(project_root, data_driven_model_path)

    model_path = os.path.join(data_driven_model_path, "best_model.pth")
    if not os.path.exists(model_path):
        raise FileNotFoundError(f"Data-driven model file not found: {model_path}")

    # Import here to avoid circular imports
    from models.basicPINNv8 import ConfigurableMLP

    # Create model with fixed architecture
    model = ConfigurableMLP(
        input_dim=10,
        hidden_layers=[128, 128],
        output_dim=4,
        activation='elu',
        dropout_rate=0.24900923127192412,
        init_method='xavier_uniform'
    )

    # Load weights
    state_dict = torch.load(model_path, map_location=device)
    model.load_state_dict(state_dict)
    model = model.double().to(device)

    # Load scaling parameters
    scaling_path = os.path.join(data_driven_model_path, "scaling_params.npz")
    if not os.path.exists(scaling_path):
        raise FileNotFoundError(f"Scaling parameters not found for data-driven model: {scaling_path}")

    scaling_data = np.load(scaling_path)
    scaling_params = {
        'Xmin': torch.from_numpy(scaling_data['Xmin']).double(),
        'Xmax': torch.from_numpy(scaling_data['Xmax']).double(),
        'ymin': torch.from_numpy(scaling_data['ymin']).double(),
        'ymax': torch.from_numpy(scaling_data['ymax']).double()
    }

    return model, scaling_params


